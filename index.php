    <div class="simple-text">
        <p>Motörhead — британская хэви-метал-группа, оказавшая большое влияние на развитие всей тяжёлой музыки, в частности, на возникновение спид-метала, хотя отнести её музыку к этому стилю можно только с натяжкой. Музыку группы, скорее, можно охарактеризовать как сочетание хард-рока, панк-рока и прото-трэша.</p>
        <p>Группа была образована в 1975 году басистом, вокалистом и автором текстов Иэном Фрейзером Килмистером по прозвищу Лемми (Ian Fraser «Lemmy» Kilmister), который на протяжении всего длительного существования группы был её единственным постоянным участником. Motörhead обычно представляла собой трио. Группа достигла наибольшей популярности в начале 1980-х годов, когда альбом Ace of Spades (1980) получил статус золотого, заняв четвёртое место в британском хит-параде, а концертный альбом No Sleep 'til Hammersmith занял первое место через пять дней после своего выхода. Эти альбомы наряду с Overkill и Bomber закрепили за Motörhead репутацию основной британской хард-рок-группы своего времени.</p>
        <p>Хотя музыку Motörhead часто относят к хеви-метал, спид-метал или трэш-метал и даже называют группу родоначальником двух последних стилей, Лемми не любил подобные ярлыки и предпочитал называть свою музыку просто — «рок-н-роллом». Темы песен группы чаще всего — это война, борьба добра со злом, злоупотребление властью, беспорядочный секс, злоупотребление наркотиками и «жизнь в дороге». Логотип группы, Snaggletooth (War-Pig) впервые появился на обложке альбома в 1977 году и с тех пор в разных вариациях присутствует на многих других альбомах группы; он был изобретён в 1977 году известным в рок-среде художником Джо Петаньо. Автор образа так прокомментировал своё творение: «Вдохновение? Ну конечно же, я изобразил разъяренного ублюдка! Такого как, например, Лемми. Я долго изучал типы черепов — и в итоге пришёл к прекрасной комбинации гориллы, волка и собаки. Плюс, конечно, рога. Ну а Лемми уже добавил шипы, цепи и прочую амуницию».</p>
